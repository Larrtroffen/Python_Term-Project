# 形状的命名
try:
    i = int(input())
except:
    print("输入的不是整数！")
if i == 3:
    print("三角形")
elif i == 4:
    print("四边形")
elif i == 5:
    print("五边形")
elif i == 6:
    print("六边形")
elif i == 7:
    print("七边形")
elif i == 8:
    print("八边形")
elif i == 9:
    print("九边形")
elif i == 10:
    print("十边形")
else:
    print("无法识别")